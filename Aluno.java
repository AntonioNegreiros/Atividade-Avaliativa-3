public class Aluno {
    int IdAluno;
    String NomeAluno;
    String DataNascimento;
    String cpf;
    int IdAlunoCurso;

public Aluno(
    int IdAluno,
    String NomeAluno,
    String DataNascimento,
    String cpf,
    int IdAlunoCurso
){
    this.IdAluno = IdAluno;
    this.NomeAluno = NomeAluno;
    this.DataNascimento = DataNascimento;
    this.cpf = cpf;
    this.IdAlunoCurso = IdAlunoCurso;
}

}