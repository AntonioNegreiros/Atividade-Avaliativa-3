public class Curso {
    int IdCurso;
    String NomeCurso;
    int CargaHoraria;



    public Curso(
        int IdCurso,
        String NomeCurso,
        int CargaHoraria
    ){
        this.IdCurso = IdCurso;
        this.NomeCurso = NomeCurso;
        this.CargaHoraria = CargaHoraria;
    }

}
